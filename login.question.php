          </p>
          <p>
          THE NEPHELAI (or Nephelae) were the Okeanid nymphs of clouds and rain who rose up from the earth-encircling river Okeanos bearing water to the heavens in cloudy pitchers. With their rains, the Nephelai nourished the earth and the fed the streams of their River-god brothers. The Nephelai were depicted as beautiful, young women pouring water from pitchers, like their sisters, the Naiades of the springs, or as women flitting across the sky with billowing robes. <i><a href="http://www.theoi.com/Nymphe/Nephelai.html" target="new">Theoi</a></i>
